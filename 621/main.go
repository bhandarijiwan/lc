// https://leetcode.com/problems/task-scheduler/

package main

import (
	"fmt"
)

func main() {
	for i := 0; i <8 ; i++ {
		fmt.Print("\"A\",")
	}
	for i := 0; i <4 ; i++ {
		fmt.Print("\"B\",")
	}
	for i := 0; i <5 ; i++ {
		fmt.Print("\"C\",")
	}
	for i := 0; i <3 ; i++ {
		fmt.Print("\"D\",")
	}
}
